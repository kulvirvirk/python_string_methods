# 1. upper() - Call this method to return an upper case version of any string.
# 2. lower() - Call this method to return a lower case version of any string. 

someString = "Lorem ipsum dolor sit amet, an postulant conclusionemque eum. Ex deleniti pertinax est, mei probo euismod intellegebat at. Fierent indoctum vel cu. Qui graeci maiorum nominavi ei, ex nonumy ponderum duo. Atqui quidam mea te. Ius accusamus cotidieque at, et duo aliquip dolores, munere iudicabit expetendis eu his. Ignota delicatissimi pri ne, magna harum te est, cu eum laudem legendos."

# 1. upper() - Call this method to return an upper case version of any string.
print(someString.upper())
print('===============================')

# note: the original string is still the same, 
print(someString)
print('===============================')

# 2. lower() - Call this method to return a lower case version of any string. 
print(someString.lower())
print('===============================')
